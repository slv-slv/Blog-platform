import { HTTP_STATUS } from './http-status-codes.js';

export enum RESULT_STATUS {
  SUCCESS = 'SUCCESS',
  CREATED = 'CREATED',
  NO_CONTENT = 'NO_CONTENT',
  BAD_REQUEST = 'BAD_REQUEST',
  UNAUTHORIZED = 'UNAUTHORIZED',
  FORBIDDEN = 'FORBIDDEN',
  NOT_FOUND = 'NOT_FOUND',
  INTERNAL_SERVER_ERROR = 'INTERNAL_SERVER_ERROR',
}

export const httpCodeByResult = (status: RESULT_STATUS): HTTP_STATUS => {
  switch (status) {
    case RESULT_STATUS.SUCCESS:
      return HTTP_STATUS.OK_200;
    case RESULT_STATUS.CREATED:
      return HTTP_STATUS.CREATED_201;
    case RESULT_STATUS.NO_CONTENT:
      return HTTP_STATUS.NO_CONTENT_204;
    case RESULT_STATUS.BAD_REQUEST:
      return HTTP_STATUS.BAD_REQUEST_400;
    case RESULT_STATUS.UNAUTHORIZED:
      return HTTP_STATUS.UNAUTHORIZED_401;
    case RESULT_STATUS.FORBIDDEN:
      return HTTP_STATUS.FORBIDDEN_403;
    case RESULT_STATUS.NOT_FOUND:
      return HTTP_STATUS.NOT_FOUND_404;
    default:
      return HTTP_STATUS.INTERNAL_SERVER_ERROR_500;
  }
};
